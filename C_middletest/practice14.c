// 증감 연산자

#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#define TAX_RATE 0.2

int main(void)
{
 
    int x = 10;
    int y = 10;

    printf("x=%d \n", x);
    printf("++의 값=%d \n", ++x);
    printf("x=%d \n\n", x); 

    printf("y=%d \n", y);
    printf("y++의 값=%d \n", y++);
    printf("y=%d \n",y);

    return 0;


}
